func CaMethod(a11, b11, c11, a21, b21, c21, a22, b22, c22 float64) (*Camx, error) {
	t := new(Camx)
	var err error
	t.Direct.Set(a11, b11, c11, a21, b21, c21, a22, b22, c22)
	t.Inverse, err = t.Direct.Inverse()
	return t, err
}

